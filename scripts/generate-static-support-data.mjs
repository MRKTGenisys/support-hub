import { readFileSync, writeFileSync } from "node:fs";
import vm from "node:vm";
import ts from "typescript";

const seedSource = readFileSync("scripts/seed-support-hub.ts", "utf8");
const start = seedSource.indexOf("const categories = [");
const end = seedSource.indexOf("async function upsertResourceFile");

if (start === -1 || end === -1 || end <= start) {
  throw new Error("Could not locate support hub seed data block.");
}

const seedDataBlock = seedSource.slice(start, end);
const compiled = ts.transpileModule(
  `${seedDataBlock}
globalThis.__supportHubSeedData = { categories, articles, pdfResources };`,
  {
    compilerOptions: {
      module: ts.ModuleKind.CommonJS,
      target: ts.ScriptTarget.ES2022,
    },
  },
).outputText;

const context = { globalThis: {} };
vm.createContext(context);
vm.runInContext(compiled, context);

const { categories, articles, pdfResources } = context.globalThis.__supportHubSeedData;
const categoryBySlug = new Map(categories.map((category) => [category.slug, category]));
const articleBySlug = new Map(articles.map((article) => [article.slug, article]));

const staticCategories = categories.map((category) => ({
  id: category.slug,
  title: category.title,
  slug: category.slug,
  description: category.description,
  iconKey: category.icon,
  sortOrder: category.sortOrder,
}));

const staticArticles = articles.map((article) => {
  const category = categoryBySlug.get(article.categorySlug);
  const relatedArticles = (article.relatedArticleSlugs || [])
    .map((slug) => articleBySlug.get(slug))
    .filter(Boolean)
    .map((relatedArticle) => ({
      title: relatedArticle.title,
      slug: relatedArticle.slug,
      category: categoryBySlug.get(relatedArticle.categorySlug)?.title || "",
    }));

  const guideLayout = article.guideLayout
    ? {
        ...article.guideLayout,
        relatedArticles,
      }
    : { enabled: false, relatedArticles };

  return {
    id: article.slug,
    title: article.title,
    subtitle: article.subtitle || article.summary,
    slug: article.slug,
    category: category
      ? {
          id: category.slug,
          title: category.title,
          slug: category.slug,
          description: category.description,
          iconKey: category.icon,
          sortOrder: category.sortOrder,
        }
      : undefined,
    summary: article.summary,
    excerpt: article.excerpt || article.summary,
    difficulty: article.difficulty || "",
    appliesTo: article.appliesTo || [],
    content: article.content || null,
    tags: article.tags || [],
    popularArticle: Boolean(article.popularArticle),
    estimatedReadTime: article.estimatedReadTime || 5,
    status: "published",
    publishedAt: article.publishedAt,
    guideLayout,
    relatedArticles,
    articleCTA: article.articleCTA || { enabled: false },
    seo: article.seo || null,
  };
});

const staticPdfResources = pdfResources.map((resource) => ({
  id: resource.slug,
  title: resource.title,
  slug: resource.slug,
  description: resource.description,
  category: resource.category,
  resourceType: resource.resourceType,
  fileUpload: null,
  tags: resource.tags || [],
  featured: Boolean(resource.featured),
  sortOrder: resource.sortOrder || 0,
  status: resource.status || "published",
  publishedAt: resource.publishedAt,
}));

const moduleSource = `// Generated by scripts/generate-static-support-data.mjs from scripts/seed-support-hub.ts.
// Used by the static Vite frontend when the live Payload API is unavailable.

export const staticCategories = ${JSON.stringify(staticCategories, null, 2)};

export const staticArticles = ${JSON.stringify(staticArticles, null, 2)};

export const staticPdfResources = ${JSON.stringify(staticPdfResources, null, 2)};
`;

writeFileSync("src/staticSupportData.js", moduleSource);

console.log(
  `Generated ${staticArticles.length} static articles, ${staticCategories.length} categories and ${staticPdfResources.length} resources.`,
);
